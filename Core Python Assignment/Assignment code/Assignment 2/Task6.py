# 6. WAP to calculate total salary of employee based on basic, da=10% of basic, ta=12% of basic, hra=15% of basic. 
basic = float(input("Enter the basic salayr of the Employee:"))

da = 0.10    # basic = 10%.
ta = 0.12    # basic = 12%.
hra = 0.15   # basic = 15%.

total_salary = basic + da + ta + hra

print(f"total salayr of Employee:, {total_salary}")
