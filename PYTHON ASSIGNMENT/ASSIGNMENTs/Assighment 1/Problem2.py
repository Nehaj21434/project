# Q2 Write a program to calculate area of rectangle
#  based on length and breadth.

# This program calculates the area of a rectangle.
# Ask the user to enter the length and breadth of the rectangle.
Length = float(input("Enter the length of the rectange:"))
Breadth = float(input("Enter the breadth of the rectangle:"))

# calculate the area of the rectangle
Area = Length * Breadth

# print the area of the rectange
print("Area of the rectange =", Area)
